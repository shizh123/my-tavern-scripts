/**
 * 根据苗广心态和千晶幻术/改嫁完成状态选择苗喧提示池
 */
function getMiaoxuanPool(data: SchemaType): string[] {
  // 掌门改嫁完成 → 池H
  if (data._已完成特殊场景['掌门改嫁']) return MIAOXUAN_POOLS.H;
  // 千晶幻术认知改写完成 → 池G
  if (data.苗广.千晶幻术.认知改写完成) return MIAOXUAN_POOLS.G;

  switch (data.苗广.心态) {
    case '正常':
      return MIAOXUAN_POOLS.A;
    case '疑惑':
      return MIAOXUAN_POOLS.B;
    case '察觉':
      return MIAOXUAN_POOLS.C;
    case '屈辱':
      return MIAOXUAN_POOLS.D;
    case '默许':
      return MIAOXUAN_POOLS.E;
    case '沉溺':
      return MIAOXUAN_POOLS.F;
    default:
      return MIAOXUAN_POOLS.A;
  }
}

/**
 * 检查苗喧彩蛋是否应触发（楼层频率控制）
 * 触发时将碎片文本写入 data._苗喧碎片，返回 true
 */
export function checkMiaoxuanEasterEgg(data: SchemaType, currentFloor: number): boolean {
  // 频率控制：距上次触发至少10楼
  if (currentFloor - data._苗喧上次触发楼层 < 10) return false;

  // 10~15楼随机触发（超过15楼强制触发）
  const gap = currentFloor - data._苗喧上次触发楼层;
  if (gap < 15 && Math.random() > 0.4) return false;

  // 优先级最低：特殊场景/千晶幻术进行中时跳过
  if (data._特殊场景.进行中) return false;
  if (data.苗广.千晶幻术.激活中) return false;

  // 选择提示池并随机选取
  const pool = getMiaoxuanPool(data);
  const hint = pool[Math.floor(Math.random() * pool.length)];

  // 更新触发楼层 + 存储碎片到状态栏显示
  data._苗喧上次触发楼层 = currentFloor;
  data._苗喧碎片 = hint;

  return true;
}

