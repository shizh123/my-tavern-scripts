/**
 * 批量道具使用事件（方式3：选中多个道具后确认使用，合并为一条叙事）
 *
 * 服装类合并为赠礼事件；其他道具使用各自独立的事件文本。
 */
export function buildBatchUseEvent(itemNames: string[], data: SchemaType): string {
  const clothingGifts = itemNames.filter(n => CLOTHING_GIFT_DESC[n]);
  const otherItems = itemNames.filter(n => !CLOTHING_GIFT_DESC[n]);

  const parts: string[] = [];

  // 服装赠礼（合并）
  if (clothingGifts.length === 1) {
    parts.push(`【赠礼】{{user}}从身后取出${CLOTHING_GIFT_DESC[clothingGifts[0]]}递给云霜凝。
[AI指令] 描写云霜凝收到这件衣物时的反应——根据信任度和防线决定接受程度。`);
  } else if (clothingGifts.length > 1) {
    const list = clothingGifts.map(n => `· ${CLOTHING_GIFT_DESC[n]}`).join('\n');
    parts.push(`【赠礼】{{user}}从身后取出几件衣物递给云霜凝：
${list}
[AI指令] 描写云霜凝看到这些衣物时的反应——根据信任度和防线决定接受程度。可以分别描写她对不同衣物的反应。`);
  }

  // 其他道具：使用各自的独立事件文本
  for (const name of otherItems) {
    // 跳过内部系统事件（双下划线包裹的名称由脚本单独处理，不需要生成事件文本）
    if (name.startsWith('__') && name.endsWith('__')) continue;
    const eventFn = itemEventMap[name];
    if (eventFn) {
      parts.push(eventFn(data));
    } else {
      parts.push(`【道具使用】使用了${name}。`);
    }
  }

  if (parts.length > 0) {
    parts.push(
      `⚠️【禁令】以上是本轮全部道具事件，AI将其自然融入叙事即可。AI严禁自行创造、添加、编造任何不在上述列表中的道具或【道具使用】事件。游戏中不存在的道具绝对不能出现。`,
    );
  }

  return parts.join('\n\n');
}
