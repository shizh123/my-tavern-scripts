/**
 * 进入神魂空间时选择记忆场景（阶段3+生效）
 * 由 index.ts 在 __神魂空间入口__ 事件时调用
 */
export function selectSoulMemory(data: SchemaType): void {
  // 阶段1-2：无记忆场景
  if (data.治疗.阶段 < 3) {
    data._神魂记忆场景 = '';
    return;
  }

  // 首次：固定触发新婚夜
  if (!data._新婚夜已触发) {
    data._神魂记忆场景 = '新婚夜';
    data._新婚夜已触发 = true;
    data._记忆进入次数['新婚夜'] = (data._记忆进入次数['新婚夜'] ?? 0) + 1;
    return;
  }

  // 后续：随机抽取（优先次数少的）
  const pool = Object.keys(SOUL_MEMORY_POOL);
  const minCount = Math.min(...pool.map(k => data._记忆进入次数[k] ?? 0));
  const candidates = pool.filter(k => (data._记忆进入次数[k] ?? 0) === minCount);
  const chosen = candidates[Math.floor(Math.random() * candidates.length)];

  data._神魂记忆场景 = chosen;
  data._记忆进入次数[chosen] = (data._记忆进入次数[chosen] ?? 0) + 1;
}

/** 获取当前记忆场景的注入词（一句话，用于快照） */
function getSoulMemorySnippet(data: SchemaType): string {
  const memory = data._神魂记忆场景;
  if (!memory || !SOUL_MEMORY_POOL[memory]) return '';
  return `\n⟨记忆场景⟩ ${SOUL_MEMORY_POOL[memory]}\n`;
}

