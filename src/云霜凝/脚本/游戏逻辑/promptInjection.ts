/** 单阶段引导数据 */
interface StageGuide {
  guidance: string; // AI引导词（核心）
  correctionHints: string[]; // 偏离时的修正台词（随机选一条注入）
  progressHint: string; // 玩家卡关时的引导提示
}

/** 单场景完整数据 */
interface SpecialSceneGuideData {
  triggerUserMessage: string; // 方式3触发时的玩家消息替换
  triggerPrefill: string; // 方式3触发时的AI预填
  stages: Record<number, StageGuide>;
}

function buildSpecialSceneGuide(data: SchemaType): string {
  const scene = data._特殊场景.进行中;
  if (!scene) return '';

  const phase = data._特殊场景.当前阶段;
  const total = data._特殊场景.总阶段数;
  const turns = data._特殊场景.当前阶段轮次 ?? 0;

  const sceneData = SPECIAL_SCENE_GUIDES[scene];
  if (!sceneData || !sceneData.stages[phase]) {
    return `\n⚠️ 【特殊场景进行中】${scene} 阶段${phase}/${total}（引导词缺失）\n`;
  }

  const stage = sceneData.stages[phase];
  let text = `\n【特殊场景·${scene}】阶段${phase}/${total - 1}（本阶段第${turns + 1}轮）\n`;
  text += stage.guidance + '\n';

  // 轮次较多时追加进度提示
  if (turns >= 2 && stage.progressHint) {
    text += `[进度提示] ${stage.progressHint}\n`;
  }

  // 轮次较多时追加修正提示（随机选一条作为参考）
  if (turns >= 3 && stage.correctionHints.length > 0) {
    const hint = stage.correctionHints[turns % stage.correctionHints.length];
    text += `[修正参考] ${hint}\n`;
  }

  text += `[规则] 玩家每轮可自由行动和对话。AI须按照上述引导方向推进剧情，但必须留出玩家互动空间，不可替代玩家行动或选择。\n`;
  return text;
}

/** 获取特殊场景的方式3触发数据（由 index.ts 调用） */
export function getSpecialSceneTrigger(sceneName: string): { userMessage: string; prefill: string } | null {
  // 特殊场景（镜前/夫前/寝取等）
  const sceneData = SPECIAL_SCENE_GUIDES[sceneName];
  if (sceneData) return { userMessage: sceneData.triggerUserMessage, prefill: sceneData.triggerPrefill };

  return null;
}

