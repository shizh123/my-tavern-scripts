/**
 * 根据阶段获取配置
 */
export function getStageConfig(stage: number): StageConfig | undefined {
  return STAGE_CONFIG.find(config => config.阶段 === stage);
}

/**
 * 根据沦陷度获取对应阶段
 */
export function getStageByCorruption(corruption: number): number {
  for (const config of STAGE_CONFIG) {
    const [min, max] = config.沦陷度范围;
    if (corruption >= min && corruption <= max) {
      return config.阶段;
    }
  }
  return 4; // 超过最大值返回最终阶段
}

/**
 * 获取阶段标题
 */
export function getStageTitle(stage: number): StageTitle {
  const config = getStageConfig(stage);
  return config?.阶段标题 || '掌控';
}

/**
 * 获取念头培育时间修正系数
 */
export function getStageTimeModifier(stage: number): number {
  const config = getStageConfig(stage);
  return config?.时间修正系数 || 1.0;
}

/**
 * 获取阶段突破剧情配置
 */
export function getStageStory(stage: number): StageStoryConfig | undefined {
  return STAGE_STORY_CONFIG.find(config => config.阶段 === stage);
}

