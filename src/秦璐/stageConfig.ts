// ============================================
// 暴露程度和妆容浓度的类型定义
// ============================================

export const EXPOSURE_LEVELS = ['保守', '正常', '清凉', '暴露', '极度暴露'] as const;
export const MAKEUP_LEVELS = ['素颜', '淡妆', '日常妆', '浓妆', '艳妆'] as const;
export const TIDINESS_LEVELS = ['整洁', '略显凌乱', '凌乱', '破损', '衣不蔽体'] as const;

export type ExposureLevel = (typeof EXPOSURE_LEVELS)[number];
export type MakeupLevel = (typeof MAKEUP_LEVELS)[number];
export type TidinessLevel = (typeof TIDINESS_LEVELS)[number];

/** 获取暴露程度的数值索引 */
export function getExposureIndex(level: ExposureLevel): number {
  return EXPOSURE_LEVELS.indexOf(level);
}

/** 获取妆容浓度的数值索引 */
export function getMakeupIndex(level: MakeupLevel): number {
  return MAKEUP_LEVELS.indexOf(level);
}

/** 获取整洁度的数值索引 */
export function getTidinessIndex(level: TidinessLevel): number {
  return TIDINESS_LEVELS.indexOf(level);
}

// ============================================
// 5阶段标题
// ============================================

export const STAGE_TITLES = ['抵抗', '动摇', '沉溺', '疯狂', '圆满'] as const;
export type StageTitle = (typeof STAGE_TITLES)[number];

// ============================================
// 阶段配置接口
// ============================================

export interface StageConfig {
  阶段: number;
  阶段标题: StageTitle;
  侵蚀度范围: [number, number];

  // 外观约束
  约束: {
    最低暴露程度: ExposureLevel;
    暴露程度范围: [ExposureLevel, ExposureLevel];
    最低妆容浓度: MakeupLevel;
    妆容浓度范围: [MakeupLevel, MakeupLevel];
    允许特殊装饰: boolean;
    允许身体改造: boolean;
  };

  // 默认外观
  默认外观: {
    服装: {
      头部: string;
      上装: string;
      下装: string;
      内衣上: string;
      内衣下: string;
      袜裤: string;
      鞋子: string;
      外套: string;
      配饰: string;
      特殊装饰: string;
      整体风格: string;
      暴露程度: ExposureLevel;
      整洁度: TidinessLevel;
    };
    妆容: {
      底妆: string;
      眼妆: string;
      唇妆: string;
      腮红: string;
      特殊妆容: string;
      浓淡程度: MakeupLevel;
      整体风格: string;
    };
    气质描述: string;
  };

  // 身体改造建议（阶段4-5）
  身体改造建议?: {
    可能出现的纹身: string[];
    可能出现的穿刺: string[];
    永久标记: string[];
  };

  // 念头培育时间修正系数
  时间修正系数: number;
}

